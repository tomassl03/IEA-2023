# Ciclos FOR
print ( 'Ciclo FOR Lista de variáveis')

lista = ['Bananas', 'Laranjas', 'Maça']
for frutas in lista:
    #Visualização de frutas
    print (frutas)
    
print ('Ciclo For Simples')
for aux in range(5):
    #Visualização de números entre 1 a 5
    print (aux)
    
print ('Ciclo For com Inicio, Fim e Incremento')
for aux in range(1, 12, 3):
    #Visualização de números de 1 a 12 com intervalo de 3
    print (aux)
